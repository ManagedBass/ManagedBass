namespace ManagedBass
{
    /// <summary>
    /// Generic class for holding tags.
    /// </summary>
    public class TagProperties<T>
    {
        /// <summary>
        /// Gets the Title.
        /// </summary>
        public T Title { get; set; }

        /// <summary>
        /// Gets the Artist.
        /// </summary>
        public T Artist { get; set; }

        /// <summary>
        /// Gets the Album.
        /// </summary>
        public T Album { get; set; }

        /// <summary>
        /// Gets the Album Artist.
        /// </summary>
        public T AlbumArtist { get; set; }

        /// <summary>
        /// Gets the Subtitle.
        /// </summary>
        public T Subtitle { get; set; }

        /// <summary>
        /// Gets the Beats per Minute (BPM).
        /// </summary>
        public T BPM { get; set; }

        /// <summary>
        /// Gets the Composer.
        /// </summary>
        public T Composer { get; set; }

        /// <summary>
        /// Gets the Copyright.
        /// </summary>
        public T Copyright { get; set; }

        /// <summary>
        /// Gets the Genre.
        /// </summary>
        public T Genre { get; set; }

        /// <summary>
        /// Gets the Grouping.
        /// </summary>
        public T Grouping { get; set; }

        /// <summary>
        /// Gets the Publisher.
        /// </summary>
        public T Publisher { get; set; }

        /// <summary>
        /// Gets the Encoder.
        /// </summary>
        public T Encoder { get; set; }

        /// <summary>
        /// Gets the Lyricist.
        /// </summary>
        public T Lyricist { get; set; }

        /// <summary>
        /// Gets the Year.
        /// </summary>
        public T Year { get; set; }

        /// <summary>
        /// Gets the Conductor.
        /// </summary>
        public T Conductor { get; set; }

        /// <summary>
        /// Gets the Track Number.
        /// </summary>
        public T Track { get; set; }

        /// <summary>
        /// Gets the Producer.
        /// </summary>
        public T Producer { get; set; }

        /// <summary>
        /// Gets the Comment.
        /// </summary>
        public T Comment { get; set; }

        /// <summary>
        /// Gets the Mood.
        /// </summary>
        public T Mood { get; set; }

        /// <summary>
        /// Gets the Rating.
        /// </summary>
        public T Rating { get; set; }

        /// <summary>
        /// Gets the ISRC.
        /// </summary>
        public T ISRC { get; set; }

        /// <summary>
        /// Gets the Remixer.
        /// </summary>
        public T Remixer { get; set; }
    }
}